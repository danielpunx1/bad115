    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        // TODO add your handling code here:
        Connection conn = null;
        OracleConnection c = new OracleConnection();
        
        combos_procedimientos objeto1 = (combos_procedimientos) materia.getSelectedItem();
        combos_procedimientos objeto2 = (combos_procedimientos) requisito.getSelectedItem();
           
        try{
            conn = c.getConnection();
          
            CallableStatement sp ;
            sp = conn.prepareCall("call sp_prerrequisito_eliminar(?,?)");
            sp.setString("codigo_asignatura1", objeto1.getdato() );
            sp.setString("materia_rrequisito1", objeto2.getdato() );
              
            if(sp.execute() )//Ejecuci√≥n del procedimiento
            {
                JOptionPane.showMessageDialog(null, "Requisito eliminado", " ", JOptionPane.INFORMATION_MESSAGE);
            }

                sp.close();
                conn.close();
                
                this.dispose();
                new prerrequisito_eliminar().setVisible(true);
     
        }catch(Exception e){
        }
    }//GEN-LAST:event_EliminarActionPerformed

    private void materiaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_materiaItemStateChanged
        // TODO add your handling code here:
        Connection conn = null;
        OracleConnection c = new OracleConnection();
        
        requisito.removeAllItems();
        combos_procedimientos objeto3 = (combos_procedimientos) materia.getSelectedItem();
        
        try{
            
            conn = c.getConnection();
            
            //LLamado del procedimiento al estilo pl/sql
            String sql="begin sp_pre_ret_eliminar(?,?); end;";
            
            CallableStatement sp;
            sp = conn.prepareCall(sql);
 
            //Parametros de entrada EL 1 INDICA PRIMER PARAMETRO
            sp.setString(1, objeto3.getdato());
            sp.registerOutParameter(2, OracleTypes.CURSOR);
            
            
            sp.execute();
            //Se obtiene el cursor en forma de ResultSet
            ResultSet rs = (ResultSet)sp.getObject(2);
           
            while (rs.next()){
                requisito.addItem( new combos_procedimientos( 1 ,rs.getString("materia_rrequisito") ));
            }
            
            rs.close();
            sp.close();
            conn.close();
        }
        catch(Exception e){
        }
    }//GEN-LAST:event_materiaItemStateChanged

